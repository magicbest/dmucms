package com.safewind.dmucms.model;

public class TeamMember {
	private String memberName;
	private String memberId;
	private String memberCalss;
	private String memberCollega;
	private String memberWork;
	public String getMemberName() {
		return memberName;
	}
	public void setMemberName(String memberName) {
		this.memberName = memberName;
	}
	public String getMemberId() {
		return memberId;
	}
	public void setMemberId(String memberId) {
		this.memberId = memberId;
	}
	public String getMemberCalss() {
		return memberCalss;
	}
	public void setMemberCalss(String memberCalss) {
		this.memberCalss = memberCalss;
	}
	public String getMemberCollega() {
		return memberCollega;
	}
	public void setMemberCollega(String memberCollega) {
		this.memberCollega = memberCollega;
	}
	public String getMemberWork() {
		return memberWork;
	}
	public void setMemberWork(String memberWork) {
		this.memberWork = memberWork;
	}
	
	
}
