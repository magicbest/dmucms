package com.safewind.dmucms.model;

public class BusinessTeacher {
	
	private Integer projectId;
	private String businessTeacherName;
	private String businessTeacherPhone;
	private String businessTeacherCompany;
	private String businessTeacherTitle;
	
	
	
	
	public Integer getProjectId() {
		return projectId;
	}
	public void setProjectId(Integer projectId) {
		this.projectId = projectId;
	}
	public String getBusinessTeacherName() {
		return businessTeacherName;
	}
	public void setBusinessTeacherName(String businessTeacherName) {
		this.businessTeacherName = businessTeacherName;
	}
	public String getBusinessTeacherPhone() {
		return businessTeacherPhone;
	}
	public void setBusinessTeacherPhone(String businessTeacherPhone) {
		this.businessTeacherPhone = businessTeacherPhone;
	}
	public String getBusinessTeacherCompany() {
		return businessTeacherCompany;
	}
	public void setBusinessTeacherCompany(String businessTeacherCompany) {
		this.businessTeacherCompany = businessTeacherCompany;
	}
	public String getBusinessTeacherTitle() {
		return businessTeacherTitle;
	}
	public void setBusinessTeacherTitle(String businessTeacherTitle) {
		this.businessTeacherTitle = businessTeacherTitle;
	}
	
	
}
