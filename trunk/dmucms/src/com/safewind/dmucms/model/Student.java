package com.safewind.dmucms.model;


public class Student {
	private String studentId ;
	private String studentName ;
	private String studentCollega;
	private String studentMajor ;
	private String studentClass ;
	private String studentSex ;
	private String studentNation ;
	private String studentBirthDay;
	private String studentPhone ;
	private String studentEmail ;
	private String studentResearch ;
	
	public String getStudentId()
	{
		return studentId;
	}
	public void setStudentId(String studentId)
	{
		this.studentId = studentId;
	}
	public String getStudentName()
	{
		return studentName;
	}
	public void setStudentName(String studentName)
	{
		this.studentName = studentName;
	}
	public String getStudentCollega()
	{
		return studentCollega;
	}
	public void setStudentCollega(String studentCollega)
	{
		this.studentCollega = studentCollega;
	}
	public String getStudentMajor()
	{
		return studentMajor;
	}
	public void setStudentMajor(String studentMajor)
	{
		this.studentMajor = studentMajor;
	}
	public String getStudentClass()
	{
		return studentClass;
	}
	public void setStudentClass(String studentClass)
	{
		this.studentClass = studentClass;
	}
	public String getStudentSex() {
		return studentSex;
	}
	public void setStudentSex(String studentSex) {
		this.studentSex = studentSex;
	}
	public String getStudentNation() {
		return studentNation;
	}
	public void setStudentNation(String studentNation) {
		this.studentNation = studentNation;
	}
	public String getStudentBirthDay() {
		return studentBirthDay;
	}
	public void setStudentBirthDay(String studentBirthDay) {
		this.studentBirthDay = studentBirthDay;
	}
	public String getStudentPhone() {
		return studentPhone;
	}
	public void setStudentPhone(String studentPhone) {
		this.studentPhone = studentPhone;
	}
	public String getStudentEmail() {
		return studentEmail;
	}
	public void setStudentEmail(String studentEmail) {
		this.studentEmail = studentEmail;
	}
	public String getStudentResearch() {
		return studentResearch;
	}
	public void setStudentResearch(String studentResearch) {
		this.studentResearch = studentResearch;
	}
	
	
	
}
